/**
 * String constants that predominantly used in tokenizer.
 *
 * @link https://www.w3.org/TR/2011/WD-html5-20110525/tokenization.html#tokenization
 *
 */

/**
 * Null representation character.
 */
export const NULL: string = '\u0000';

/**
 * Ampersand character.
 */
export const AMPERSAND: string = '\u0026';

/**
 * Less that sign character.
 */
export const LESS_THAN_SIGN: string = '\u003C';

/**
 * Exclamation mark character.
 */
export const EXCLAMATION_MARK: string = '\u0021';

/**
 * Solidus character.
 */
export const SOLIDUS: string = '\u002F';

/**
 * Question mark character.
 */
export const QUESTION_MARK: string = '\u003F';

/**
 * Greater than sign character.
 */
export const GREATER_THAN_SIGN: string = '\u003E';

/**
 * Character tabulation.
 */
export const CHARACTER_TABULATION: string = '\u0009';

/**
 * Line feed character.
 */
export const LINE_FEED: string = '\u000A';

/**
 * Form feed character.
 */
export const FORM_FEED: string = '\u000C';

/**
 * Space character.
 */
export const SPACE: string = '\u0020';

/**
 * Quotation mark character.
 */
export const QUOTATION_MARK: string = '\u0022';

/**
 * Apostrophe character.
 */
export const APOSTROPHE: string = '\u0027';

/**
 * Equals sign character.
 */
export const EQUALS_SIGN: string = '\u003D';

/**
 * Grave accent character.
 */
export const GRAVE_ACCENT: string = '\u0060';

/**
 * Hyphen minus character.
 */
export const HYPHEN_MINUS: string = '\u002D';

/**
 * Left square bracket character.
 */
export const LEFT_SQUARE_BRACKET: string = '\u005B';

/**
 * Right square bracket character.
 */
export const RIGHT_SQUARE_BRACKET: string = '\u005D';

/**
 * Left curly bracket character.
 */
export const LEFT_CURLY_BRACKET: string = '\u007B';

/**
 * Right curly bracket character.
 */
export const RIGHT_CURLY_BRACKET: string = '\u007D';

/**
 * Null replacement character.
 */
export const NULL_REPLACEMENT: string = '\uFFFD';
